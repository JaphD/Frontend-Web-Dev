var num1 = 15;
var num2 = 30;

// =
num1 = 20;

// +=
num1 += 10; // num1 = num1 + 10
num1 += num2; // num1 num1 + num2
console.log(num1);

// -=
num1 -= 10; //num1 = num1 - 10
num1 -= num2 //num1 = num1 - num2
console.log(num1);

// *=
num1 *= 20 //num1 = num1 * 20
console.log(num1)

// /=
num1 /= 10;
console.log(num1);

// %=
num1 %= 10; //num1 = num1 % 10
console.log(num1);

